    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        FileNameExtensionFilter filtro= new FileNameExtensionFilter(".jpg",".png");
        JFileChooser jfile = new JFileChooser();
        jfile.setFileFilter(filtro);
        int resultado = jfile.showOpenDialog(this);

        if (resultado != JFileChooser.CANCEL_OPTION) {
            File fileName = jfile.getSelectedFile();
            
            this.visualizadorImagen(lblImagen, fileName.getAbsolutePath());

        }


    }//GEN-LAST:event_jMenuItem1ActionPerformed

